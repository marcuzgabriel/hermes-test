import * as h from './bench-helpers';

test('add basics', () => { expect(h.add(2, 3)).toBe(5); expect(h.add(-1, 1)).toBe(0); });
test('add large', () => { expect(h.add(1e6, 1e6)).toBe(2e6); });
test('sub basics', () => { expect(h.sub(10, 3)).toBe(7); });
test('sub negative', () => { expect(h.sub(0, 5)).toBe(-5); });
test('mul basics', () => { expect(h.mul(3, 4)).toBe(12); expect(h.mul(0, 5)).toBe(0); });
test('mul negative', () => { expect(h.mul(-2, 3)).toBe(-6); });
test('div basics', () => { expect(h.div(10, 2)).toBe(5); });
test('div by zero', () => { expect(h.div(1, 0)).toBe(0); });
test('mod basics', () => { expect(h.mod(10, 3)).toBe(1); });
test('pow basics', () => { expect(h.pow(2, 10)).toBe(1024); });
test('clamp low', () => { expect(h.clamp(-5, 0, 10)).toBe(0); });
test('clamp mid', () => { expect(h.clamp(5, 0, 10)).toBe(5); });
test('clamp high', () => { expect(h.clamp(15, 0, 10)).toBe(10); });
test('lerp start', () => { expect(h.lerp(0, 10, 0)).toBe(0); });
test('lerp end', () => { expect(h.lerp(0, 10, 1)).toBe(10); });
test('lerp mid', () => { expect(h.lerp(0, 10, 0.5)).toBe(5); });
test('abs positive', () => { expect(h.abs(5)).toBe(5); });
test('abs negative', () => { expect(h.abs(-5)).toBe(5); });
test('sign positive', () => { expect(h.sign(5)).toBe(1); });
test('sign negative', () => { expect(h.sign(-5)).toBe(-1); });
test('sign zero', () => { expect(h.sign(0)).toBe(0); });
test('isEven true', () => { expect(h.isEven(4)).toBe(true); });
test('isEven false', () => { expect(h.isEven(3)).toBe(false); });
test('isOdd true', () => { expect(h.isOdd(3)).toBe(true); });
test('isOdd false', () => { expect(h.isOdd(4)).toBe(false); });
test('factorial 0', () => { expect(h.factorial(0)).toBe(1); });
test('factorial 5', () => { expect(h.factorial(5)).toBe(120); });
test('gcd', () => { expect(h.gcd(12, 8)).toBe(4); });
test('lcm', () => { expect(h.lcm(4, 6)).toBe(12); });
test('fibonacci 0', () => { expect(h.fibonacci(0)).toBe(0); });
test('fibonacci 10', () => { expect(h.fibonacci(10)).toBe(55); });
test('isPrime 2', () => { expect(h.isPrime(2)).toBe(true); });
test('isPrime 4', () => { expect(h.isPrime(4)).toBe(false); });
test('isPrime 97', () => { expect(h.isPrime(97)).toBe(true); });
test('reverse string', () => { expect(h.reverse('hello')).toBe('olleh'); });
test('reverse empty', () => { expect(h.reverse('')).toBe(''); });
test('isPalindrome true', () => { expect(h.isPalindrome('racecar')).toBe(true); });
test('isPalindrome false', () => { expect(h.isPalindrome('hello')).toBe(false); });
test('capitalize', () => { expect(h.capitalize('hello')).toBe('Hello'); });
test('camelCase', () => { expect(h.camelCase('foo-bar')).toBe('fooBar'); });
test('snakeCase', () => { expect(h.snakeCase('fooBar')).toBe('foo_bar'); });
test('repeat', () => { expect(h.repeat('ab', 3)).toBe('ababab'); });
test('truncate short', () => { expect(h.truncate('hi', 10)).toBe('hi'); });
test('truncate long', () => { expect(h.truncate('hello world!', 8)).toBe('hello...'); });
test('unique', () => { expect(h.unique([1, 2, 2, 3])).toEqual([1, 2, 3]); });
test('sum', () => { expect(h.sum([1, 2, 3])).toBe(6); });
test('mean', () => { expect(h.mean([2, 4, 6])).toBe(4); });
test('range', () => { expect(h.range(0, 5)).toEqual([0, 1, 2, 3, 4]); });
test('chunk', () => { expect(h.chunk([1, 2, 3, 4], 2)).toEqual([[1, 2], [3, 4]]); });
test('flatten', () => { expect(h.flatten([[1], [2, 3]])).toEqual([1, 2, 3]); });
